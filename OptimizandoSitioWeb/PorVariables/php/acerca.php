			<h2>¿Por qué es importante HTML5?</h2>
			<p>
				HTML5 es la piedra angular de la plataforma web abierta de la W3C, un marco diseñado para apoyar la innovación y fomentar el potencial que la web tiene para ofrecer. Es el presente de la web y si no lo has entendido, ya eres parte de la vieja escuela.
			</p>
			<hr />
			<p>
				Entender HTML5 es entender que hoy nos conectamos desde diferentes dispositivos. Define los nuevos estándares de desarrollo web, tanto para equipos de escritorio, como para móviles. Se abren las puertas a nuevas opciones de diseño y dinámicas de información. 
			</p>
			<hr />
			<p>
				Se acabó la era de los webmasters y hoy hablamos de equipos transdisciplinarios: frontends, backends, mobile devs, responsive designers, comunity managers, digital marketers, sysadmins, dba's y arquitectos de información que día a día contribuyen y colaboran en los proyectos que estan reinventando la web.
			</p>
